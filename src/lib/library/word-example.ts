// Picking ONE Tatoeba sentence to show on a word's Library page.
//
// WHY THIS IS A BUILD STEP AND NOT A RUNTIME LOOKUP
// =================================================
// The corpus lives in grammar-corpus.json, 1.8 MB, and nothing on the Library
// entry page imports it today. Indexing it at runtime — however lazily, however
// well memoised — would put all 1.8 MB into the client bundle of a page that
// needs one sentence out of 8,689, on top of the 3 MB of vocab.json it already
// carries. The build step in scripts/build-word-examples.ts runs the chooser
// below once, offline, and emits src/data/generated/word-examples.json: the
// chosen sentence for each word that has one, and nothing else. That file is
// ~15% the size of the corpus and the page pays a single Map lookup.
//
// The chooser therefore lives HERE rather than in the script, so the tests
// exercise the same code the artifact was generated by.
//
// MATCHING IS ON THE WRITTEN FORM, EXACTLY
// ========================================
// `Example.v` holds content lemmas as written. A word is a candidate for a
// sentence when its `keb` is one of them. Kana-only words (JMdict `uk`, and
// anything with no kanji spelling) have keb === reb, so they match on the same
// key with no special case.
//
// Matching a kanji word's READING as well would add ~120 words of coverage and
// a class of wrong sentences with it: 蚊, 科 and 課 are all か, 億 and 奥 are
// both おく, and a corpus lemma spelled in kana cannot be told apart from any of
// them. A silently mis-attributed example is worse than an absent one on a page
// whose whole job is to be checkable against reality, so reading matches are
// not admitted. This costs 1.0 percentage point of coverage; see
// word-example.test.ts, which pins both numbers.

import type { Example } from "../../data/grammar/corpus.ts";

/**
 * The score given to a content lemma that is in no vocabulary row.
 *
 * Bigger than any real `beginnerRank` (the field is a total order over 12,553
 * words, so it never exceeds that) and bigger by a wide margin, so the two can
 * never be confused when debugging a score. The point is that an unlisted lemma
 * is HARDER than the hardest listed word, not easier: score by skipping them
 * and a sentence made entirely of proper nouns — トム, ロンドン — scores as the
 * easiest thing in the corpus, which is the opposite of true.
 */
export const UNRANKED = 1_000_000;

/** `beginnerRank` by written form. The caller supplies it so this module never
 * imports vocab.json, which matters at build time and in tests. */
export type RankOf = (lemma: string) => number | undefined;

/**
 * How hard this sentence is, as the rank of the hardest word in it.
 *
 * THE TARGET WORD IS EXCLUDED. It is in `v` for every candidate by
 * construction, so including it would make it the maximum on every candidate
 * for any word rarer than the sentences around it — and then every candidate
 * ties and the ranking collapses to "shortest". The owner asked for sentences
 * whose vocabulary is common; on 沿う that means the OTHER words, because 沿う
 * itself is not negotiable. So the max is taken over the rest.
 */
export function hardestRank(ex: Example, target: string, rankOf: RankOf): number {
  let worst = 0;
  for (const lemma of ex.v) {
    if (lemma === target) continue;
    worst = Math.max(worst, rankOf(lemma) ?? UNRANKED);
  }
  return worst;
}

/**
 * The one sentence to show for `target`, or null if there is none.
 *
 * Ordered by hardest word (commonest wins), then by token count (shorter wins),
 * then by Tatoeba id. The last key is what makes this a total order: two
 * sentences can agree on both of the first two, and a pick that depends on
 * corpus iteration order is a pick that can move when the corpus is rebuilt.
 */
export function chooseExample(
  candidates: readonly Example[],
  target: string,
  rankOf: RankOf,
): Example | null {
  let best: Example | null = null;
  let bestScore = Infinity;
  for (const ex of candidates) {
    const score = hardestRank(ex, target, rankOf);
    if (best === null || score < bestScore) {
      best = ex;
      bestScore = score;
      continue;
    }
    if (score > bestScore) continue;
    if (ex.n !== best.n) {
      if (ex.n < best.n) best = ex;
      continue;
    }
    if (ex.id < best.id) best = ex;
  }
  return best;
}

/** Every sentence containing each written form, keyed by that form. Built once,
 * at build time, over the whole corpus. */
export function indexByWord(corpus: readonly Example[]): Map<string, Example[]> {
  const map = new Map<string, Example[]>();
  for (const ex of corpus) {
    for (const lemma of ex.v) {
      const list = map.get(lemma);
      if (list) list.push(ex);
      else map.set(lemma, [ex]);
    }
  }
  return map;
}
